package judge;

public class Edificios  {
	private int inicio;
	private int fin;
	public Edificios(int ini, int fin) {
		this.inicio=ini;
		this.fin=fin;
	}
	public int getInicio() {
		return inicio;
	}
	public void setInicio(int inicio) {
		this.inicio = inicio;
	}
	public int getFin() {
		return fin;
	}
	public void setFin(int fin) {
		this.fin = fin;
	}


}
