package judge;

public class Conferencias  {
	private long inicio;
	private long fin;
	public Conferencias(long ini, long fin) {
		this.inicio=ini;
		this.fin=fin;
	}
	public long getInicio() {
		return inicio;
	}
	public void setInicio(long inicio) {
		this.inicio = inicio;
	}
	public long getFin() {
		return fin;
	}
	public void setFin(long fin) {
		this.fin = fin;
	}


}
